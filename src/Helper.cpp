void Helper::aesEncrypt(char *plainText, byte *output) {
  byte encryptIv[] = {0xda, 0x1a, 0x55, 0x73, 0x49, 0xf2, 0x5c, 0x64,
                      0x1b, 0x1a, 0x21, 0xd2, 0x6f, 0x5b, 0x21, 0x8a};
  mbedtls_aes_context aes;
  mbedtls_aes_init(&aes);
  mbedtls_aes_setkey_enc(&aes, (const byte *)key, 128);
  mbedtls_aes_crypt_cbc(&aes, MBEDTLS_AES_ENCRYPT, 32, encryptIv,
                        (const byte *)plainText, output);
  mbedtls_aes_free(&aes);
}

void Helper::aesDecrypt(byte *cryptedText, byte *output) {
  byte decryptIv[] = {0xda, 0x1a, 0x55, 0x73, 0x49, 0xf2, 0x5c, 0x64,
                      0x1b, 0x1a, 0x21, 0xd2, 0x6f, 0x5b, 0x21, 0x8a};
  mbedtls_aes_context aes;
  mbedtls_aes_init(&aes);
  mbedtls_aes_setkey_dec(&aes, (const byte *)key, 128);
  mbedtls_aes_crypt_cbc(&aes, MBEDTLS_AES_DECRYPT, 32, decryptIv,
                        (const byte *)cryptedText, output);
  mbedtls_aes_free(&aes);
}

void Helper::getNtpTime(const char *ntpServer, long gmtOffset_sec) {
  Serial.println("Fetching time from NTP...");
  time_t now;
  struct tm new_ts;
  const int daylightOffset_sec = 0;

  // Config time using NTP
  configTime(gmtOffset_sec, daylightOffset_sec, ntpServer);

  // Get RTC time
  getLocalTime(&new_ts);
  time(&now);
  char timeStr[64];
  strftime(timeStr, sizeof(timeStr), "%A, %B %d %Y %H:%M:%S", &new_ts);
  Serial.print("Current time obtained from RTC after NTP config is: ");
  Serial.println(timeStr);
}

void Helper::getTimestamp(byte *ts) {
  time_t now;
  time(&now);
  // BigEndian
  ts[3] = now & 0xFF;
  ts[2] = (now >> 8) & 0xFF;
  ts[1] = (now >> 16) & 0xFF;
  ts[0] = (now >> 24) & 0xFF;
}
